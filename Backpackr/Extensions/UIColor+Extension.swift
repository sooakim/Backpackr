import Foundation
import UIKit

extension UIColor{
    class var coralPink: UIColor{
        return UIColor(red: 255/255, green: 88/255, blue: 108/255, alpha: 1)
    }
    class var dark: UIColor{
        return UIColor(red: 20/255, green: 20/255, blue: 40/255, alpha: 1)
    }
    class var darkSkyBlue: UIColor{
        return UIColor(red: 74/255, green: 144/255, blue: 226/255, alpha: 1)
    }
    class var xdark: UIColor{
        return UIColor(red: 24/255, green: 24/255, blue: 40/255, alpha: 1)
    }
    class var blueyGrey: UIColor{
        return UIColor(red: 171/255, green: 171/255, blue: 196/255, alpha: 1)
    }
    class var darkBlueGrey: UIColor{
        return UIColor(red: 24/255, green: 24/255, blue: 80/255, alpha: 1)
    }
    class var darkNavyBlue: UIColor{
        return UIColor(red: 0, green: 0, blue: 10/255, alpha: 1)
    }
    class var paleGreyTwo: UIColor{
        return UIColor(red: 236/255, green: 236/255, blue: 245/255, alpha: 1)
    }
    class var paleGrey: UIColor{
        return UIColor(red: 246/255, green: 246/255, blue: 250/255, alpha: 1)
    }
    class var coolGrey: UIColor{
        return UIColor(red: 163/255, green: 163/255, blue: 181/255, alpha: 1)
    }
}
